#!/usr/bin/python2 

"""
Write two functions:

    to_int() : A function to convert a string to an integer.
    to_str() : A function to convert an integer to a string.

"""

def to_int(string):
    return int(string)

def to_str(integer):
    return str(integer)

print(to_int("10"))
print(to_str(5))
